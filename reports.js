onAuthStateChanged(auth, async (user) => {
    if (user) {
        // Calculate metrics from Real Database
        const q = query(collection(db, "events"), where("organiserId", "==", user.uid));
        const querySnapshot = await getDocs(q);

        let eventCount = 0;
        let slotCount = 0;

        querySnapshot.forEach((doc) => {
            const data = doc.data();
            eventCount++;
            slotCount += parseInt(data.totalSlots || 0);
        });

        // Update the screen numbers
        document.getElementById('totalEvents').textContent = eventCount;
        document.getElementById('totalSlots').textContent = slotCount;
    }
});